import { useRef, useEffect } from "react";
import ShiftingLink from "./ShiftingLink";

const Hero = () => {
  const imageContainerRef = useRef<HTMLDivElement>(null);
  const imageRef = useRef<HTMLImageElement>(null);
  const headerRef = useRef<HTMLDivElement>(null);

  // Wrap each character in a span (for fade-in effect)
  // Wrap each word in a span (for hover effect)
  // Add a custom stagger variable to each character for animation
  const constructHeader = (text: string) => {
    const staggerOrder = [0.65, 0.2, 0.5, 0.75, 0.85, 0.1, 0.25, 0];
    const words = text.split(" ");
    const wordsArray: Array<Array<JSX.Element>> = [];

    for (const word of words) {
      const chars: Array<JSX.Element> = [];
      for (let i = 0; i < word.length; i++) {
        chars.push(
          <span
            key={i}
            className="hero-header-char"
            style={{
              ["--stagger" as string]: `${staggerOrder[i]}s`,
            }}
          >
            {word[i]}
          </span>
        );
      }
      wordsArray.push(chars);
    }

    const header = [];
    for (let i = 0; i < wordsArray.length; i++) {
      header.push(
        <span
          className="hero-header-word"
          key={i}
          onMouseEnter={() => {
            if (imageRef.current) {
              imageRef.current.src = `./hero/${i}.jpg`;
            }
            if (imageContainerRef.current) {
              imageContainerRef.current.classList.add("active");
            }
          }}
          onMouseLeave={() => {
            if (imageContainerRef.current) {
              imageContainerRef.current.classList.remove("active");
            }
          }}
        >
          {wordsArray[i]}
        </span>
      );
      if (i < wordsArray.length - 1) {
        header.push(<span key={i + wordsArray.length}>&nbsp;</span>);
      }
    }
    return header;
  };

  // Enable hover effects on header after 2 seconds
  useEffect(() => {
    setTimeout(() => {
      if (headerRef.current) {
        headerRef.current.style.pointerEvents = "all";
      }
    }, 2000);
  }, []);

  return (
    <div id="hero">
      <div id="hero-image-container" ref={imageContainerRef}>
        <img alt="Background image" id="hero-image" ref={imageRef} />
        {maskSvg}
      </div>
      <div id="hero-header" ref={headerRef}>
        <h1>{constructHeader("CAPTURE YOUR CULTURE")}</h1>
      </div>
      {flashSvg}
      <span id="hero-scroll">SCROLL DOWN</span>
      <div id="hero-links">
        <ShiftingLink
          text="SEE OUR WORK"
          stagger
          href="work"
          className="hero-link"
        />
        <ShiftingLink
          text="CONTACT US"
          stagger
          href="contact"
          className="hero-link"
        />
      </div>
    </div>
  );
};

